
public class tiposprimitivos {
     public static void main(String[] args){   
    // declara um int e atribui um valor           
        int idade = 25;   
        //declara um float e depois atribui um valor
        float valor;
        valor= 1.99f;
        //declarando um boolean
        boolean verdadeiroOuFalso = false;
        verdadeiroOuFalso = true;
        //declarando um char
        char letraA = 'A'; 
        letraA = 65; // valor ASCII para 'A'
        letraA = '\u0041'; //valor Unicode para 'A'
        //declarando um byte
        byte b =127;
        // declarando um short
        short s = 1024;
        //declarando um long
        long l = 1234567890;
        //declarando um double
        double d = 100.0;
        // declaração multipla
        int var1=0, var2=1, var3=2, var4=3;
        System.out.println("valor da variavel idade:"+idade);
        System.out.println("valor da variavel valor:"+valor);
        System.out.println("valor da variavel verdadeiro ou falso:"+verdadeiroOuFalso);
        System.out.println("valor da variavel letra A:"+letraA);
        System.out.println("valor da variavel b :"+b);
        System.out.println("valor da variavel s:"+s);
        System.out.println("valor da variavel l:"+l);
        System.out.println("valor da variavel d:"+d);
        System.out.println("valor da variavel var1 :"+var1);
        System.out.println("valor da variavel var2 :"+var2);
        System.out.println("valor da variavel var3 :"+var3);
        System.out.println("valor da variavel var4 :"+var4);
    }
}

